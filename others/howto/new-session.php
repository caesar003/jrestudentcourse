<h4>Headers</h4>
<p>Use number sign <code>#</code> to create headers started from one as header one (the largest) to six as the smallest (which is actually smaller than normal paragraph text)</p>
<pre>
# header 1
## header 2
### header 3
#### header 4
##### header 5
###### header 6
</pre>
<p>The above markup gives us this:</p>
<h1>header 1</h1>
<h2>header 2</h2>
<h3>header 3</h3>
<h4>header 4</h4>
<h5>header 5</h5>
<h6>header 6</h6>
<hr>
<h4>Lists</h4>
<h5>Numbered list</h5>
<p>Simply write number followed by a period and a whitespace to create a numbered list.</p>
<pre>
1. item 1
2. item 2
3. item 3
7. this is item 4
8. this would be item 5
</pre>
<p>This is what we get : </p>
<ol>
<li>item 1</li>
<li>item 2</li>
<li>item 3</li>
<li>this is item 4</li>
<li>this would be item 5</li>
</ol>
