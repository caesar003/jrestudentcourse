<h2>Syllabus</h2>
<p>Once we register a new student, they don't have any syllabus yet and we need to create one for them. </p>
<img width="100%" src="../others/howto/images/no_syllabus.png">
<p>We can click on one of these five levels to see what each of them consist of, </p>
<img width="100%" src="../others/howto/images/no_syllabus_2.png">
<p>Click continue to create the syllabus, then confirm it if everything is okay. </p>
<img width="100%" src="../others/howto/images/create_syllabus_confirm.png">
<p>Once that's done, we have something similar to this.</p>
<img width="100%" src="../others/howto/images/syllabus.png">
<p>Here we can do several things, </p>
<ul>
  <li>Click the little black box at the end of every topics or indicator to indicate the discussed topics (it is marked by line-through and different font color)</li>
  <li>Pairs of ovals at the end of topics are intended to put students score (usually after test) on that particular topic. </li>
  <li>Purple button on the top right of the page is used to change topics for the student, </li>
</ul>
